"""Unit tests for the MirroredCircuits benchmark in QCMet."""

import json

import pytest
from qiskit import QiskitError, QuantumCircuit
from qiskit.quantum_info import Clifford, Statevector, random_clifford

from qcmet import AerSimulator, FileManager, MirroredCircuits


@pytest.fixture
def mirrored_circuits_instance():
    """Fixture to create a MirroredCircuits instance."""
    qubits = 2
    num_circuits = 3
    m = 2
    return MirroredCircuits(qubits=qubits, num_circuits=num_circuits, m=m, seed=42)


def test_initialization_with_random_cliffords():
    """Test initialization when no custom Clifford operators are provided."""
    mc = MirroredCircuits(qubits=2, num_circuits=2, m=3, seed=123)
    assert mc.num_qubits == 2
    assert mc.config["num_circuits"] == 2
    assert len(mc.clifford_operators_base_circuit) == 3
    assert isinstance(mc.clifford_operators_base_circuit[0], Clifford)


def test_initialization_with_custom_cliffords():
    """Test initialization when a custom list of Clifford operators is provided."""
    custom_cliffords = [random_clifford(2) for _ in range(2)]
    mc = MirroredCircuits(
        qubits=2, num_circuits=2, clifford_operators=custom_cliffords, seed=123
    )
    assert mc.clifford_operators_base_circuit == custom_cliffords


@pytest.mark.parametrize("bad_m", [None, 1.0, "3", [3], {"m": 3}])
def test_mirrored_circuits_raises_typeerror_when_m_not_int(bad_m):
    """Test that __init__ raises TypeError if m is not exactly an int."""
    qubits = 3
    num_circuits = 5
    # We explicitly set clifford_operators=None to trigger the m-type check path
    with pytest.raises(TypeError) as excinfo:
        MirroredCircuits(
            qubits=qubits,
            num_circuits=num_circuits,
            m=bad_m,
            clifford_operators=None,
            seed=1234,  # deterministic RNG; not essential for this path
        )
    # Optional: assert on the error message content to ensure specificity
    assert "must be specified as an integer" in str(excinfo.value)


def make_instance_with_base_cliffords(n_qubits=2, seed=42, base_seed=100):
    """Construct a MirroredCircuits instance with a known base composition of Cliffords."""
    base_cliffords = [
        random_clifford(n_qubits, seed=base_seed),
        random_clifford(n_qubits, seed=base_seed + 1),
    ]
    return MirroredCircuits(
        qubits=n_qubits,
        num_circuits=1,
        m=len(base_cliffords),
        clifford_operators=base_cliffords,
        seed=seed,
    )


@pytest.mark.parametrize(
    "case_name, expected_exception",
    [
        (
            "non_clifford_input_raises_qiskiterror",
            QiskitError,
        ),
        (
            "mismatching_clifford_raises_valueerror",
            ValueError,
        ),
    ],
)
def test_generate_random_mirrored_circuit_exceptions(case_name, expected_exception):
    """Test exceptions raised by mirrored circuit generation.

    1) Non-Clifford input -> QiskitError raised by Clifford(input_circuit).
    2) Clifford input that doesn't match base composition -> ValueError.

    """
    n_qubits = 2
    mc = make_instance_with_base_cliffords(n_qubits=n_qubits, seed=7, base_seed=50)

    # Build the input circuit for this case
    if case_name == "non_clifford_input_raises_qiskiterror":
        # Includes a T gate -> not Clifford
        input_circuit = QuantumCircuit(n_qubits)
        input_circuit.h(0)
        input_circuit.t(1)
        input_circuit.cx(0, 1)
    else:
        input_circuit = random_clifford(n_qubits, seed=999).to_circuit()

    with pytest.raises(expected_exception):
        mc._generate_random_mirrored_circuit(input_circuit)


def test_generate_random_mirrored_circuit(mirrored_circuits_instance):
    """Test the generation of a single mirrored circuit."""
    base_circuit = QuantumCircuit(mirrored_circuits_instance.num_qubits)
    for clifford in mirrored_circuits_instance.clifford_operators_base_circuit:
        base_circuit.compose(clifford.to_circuit(), inplace=True)

    mirrored_circuit = mirrored_circuits_instance._generate_random_mirrored_circuit(
        base_circuit
    )
    assert isinstance(mirrored_circuit, QuantumCircuit)
    assert mirrored_circuit.num_qubits == mirrored_circuits_instance.num_qubits


def test_generate_circuits_structure(mirrored_circuits_instance):
    """Test the structure of circuits generated by _generate_circuits()."""
    circuits = mirrored_circuits_instance._generate_circuits()
    assert isinstance(circuits, list)
    assert len(circuits) == mirrored_circuits_instance.config["num_circuits"]
    for circ_dict in circuits:
        assert "circuit" in circ_dict
        assert isinstance(circ_dict["circuit"], QuantumCircuit)


def test_generate_expected_bitstrings(mirrored_circuits_instance):
    """Test expected bitstring generation for mirrored circuits."""
    mirrored_circuits_instance.generate_circuits()
    mirrored_circuits_instance._generate_expected_bitstrings()
    assert "expected_bitstrings" in mirrored_circuits_instance.experiment_data
    assert len(
        mirrored_circuits_instance.experiment_data["expected_bitstrings"]
    ) == len(mirrored_circuits_instance.circuits)
    for bitstring in mirrored_circuits_instance.experiment_data["expected_bitstrings"]:
        assert isinstance(bitstring, str)


@pytest.mark.parametrize(
    "build_circuit, expected",
    [
        # Single-qubit X then measure -> should deterministically yield '1'
        (
            lambda: (lambda qc: (qc.x(0), qc.measure_all(), qc)[-1])(QuantumCircuit(1)),
            "1",
        ),
        # Single-qubit identity then measure -> should deterministically yield '0'
        (
            lambda: (lambda qc: (qc.measure_all(), qc)[-1])(QuantumCircuit(1)),
            "0",
        ),
    ],
)
def test_generate_expected_bitstrings_on_specific_circuits(build_circuit, expected):
    """Test that a circuit with known deterministic outcome produces correct bitstring."""
    base_cliffords = [random_clifford(1, seed=123)]
    mc = MirroredCircuits(
        qubits=1,
        num_circuits=1,
        m=len(base_cliffords),
        clifford_operators=base_cliffords,
        seed=42,
    )

    # Inject a specific, known circuit into experiment_data
    qc = build_circuit()
    mc.experiment_data = [qc]

    mc._generate_expected_bitstrings()

    assert "expected_bitstrings" in mc.experiment_data
    expected_list = mc.experiment_data["expected_bitstrings"].tolist()
    assert len(expected_list) == 1  # Should produce exactly one expected bitstring.
    assert expected_list[0] == expected


def test_generate_expected_bitstrings_raises_qiskiterror_on_nondeterministic_output():
    """Test that _generate_expected_bitstrings raises QiskitError when output nondeterministic."""
    n_qubits = 1
    base_cliffords = [random_clifford(n_qubits, seed=123)]
    mc = MirroredCircuits(
        qubits=n_qubits,
        num_circuits=1,
        m=len(base_cliffords),
        clifford_operators=base_cliffords,
        seed=42,
    )

    # Build a 1-qubit circuit that yields a superposition (non-deterministic outcome)
    # H|0> -> (|0> + |1>)/sqrt(2)
    qc = QuantumCircuit(n_qubits)
    qc.h(0)
    qc.measure_all()

    # Inject the experiment data with our circuit
    mc.experiment_data = [qc]

    # Calling the method should raise QiskitError due to multiple outcomes
    with pytest.raises(QiskitError) as _:
        mc._generate_expected_bitstrings()


def test_analyze_polarization(mirrored_circuits_instance):
    """Test the polarization analysis logic for the noiseless case.

    Checks that average polarization is roughly equal to 1.
    """
    mirrored_circuits_instance.generate_circuits()
    mirrored_circuits_instance._generate_expected_bitstrings()

    # set exact measurement_outputs
    measurement_outputs = []
    for c in mirrored_circuits_instance.experiment_data["circuit"]:
        state = Statevector.from_int(
            0, 2**mirrored_circuits_instance.num_qubits
        ).evolve(c.remove_final_measurements(False))
        probs = AerSimulator.reverse_bitstrings(state.probabilities_dict())
        measurement_outputs.append(probs)

    mirrored_circuits_instance.experiment_data["circuit_measurements"] = (
        measurement_outputs
    )

    mirrored_circuits_instance._runtime_params = {"num_shots": 1}

    result = mirrored_circuits_instance._analyze()
    assert "J" in result
    assert 0.99999 <= result["J"] <= 1.0  # Should be close to perfect polarization


def test_config_clifford_output(tmp_path):
    """Test correct clifford output for config file."""
    qc = QuantumCircuit(1)
    qc.x(0)
    save_path = FileManager(benchmark_name="mirror", run_id="test", base_path=tmp_path)
    experiment = MirroredCircuits(
        qubits=1,
        num_circuits=1,
        m=1,
        clifford_operators=[Clifford(qc)],
        save_path=save_path,
    )
    experiment.generate_circuits()

    with open(
        save_path.base_path / "mirror_test" / "config" / "experiment_config.json", "r"
    ) as f:
        config = json.load(f)
    assert any(
        s in config["cliffords_base_circuit"][0]["stabilizer"] for s in ["-Z", "+Z"]
    )
    assert any(
        d in config["cliffords_base_circuit"][0]["destabilizer"] for d in ["-X", "+X"]
    )
